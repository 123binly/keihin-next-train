// JR official timetable data for Kamata Station.
// Compact storage: each hour contains departure minutes.

function expandTimetable(hourlyData) {
  return Object.entries(hourlyData).flatMap(([hour, minutes]) =>
    minutes.map(
      (minute) => `${hour}:${String(minute).padStart(2, "0")}`
    )
  );
}

